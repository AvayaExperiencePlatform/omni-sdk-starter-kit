var createError = require('http-errors');
var express = require('express');
var path = require('path');
var cookieParser = require('cookie-parser');
var logger = require('morgan');

var indexRouter = require('./routes/index');
var configRouter = require('./routes/configs');
var deviceRegistrationRouter = require('./routes/device-registrations');
var notificationsRouter = require('./routes/notifications');

var app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

app.use(logger('dev'));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', indexRouter);
app.use('/users', usersRouter);
app.use('/books', booksRouter);
app.use('/books:search', booksRouter);
app.use('/custom-notification-connector/configs', configRouter);
app.use('/custom-notification-connector/device-registrations', deviceRegistrationRouter);
app.use('/custom-notification-connector/device-registrations:search', deviceRegistrationRouter);
app.use('/custom-notification-connector/notifications', notificationsRouter);
app.use('/custom-notification-connector/axp-callback/notifications', notificationsRouter);

// catch 404 and forward to error handler
app.use(function(req, res, next) {
  next(createError(404));
});

// error handler
app.use(function(err, req, res, next) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;
